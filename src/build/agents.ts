import { join } from "node:path";
import { DATASET_DIR, SKILL_DIR, SOURCES_DIR } from "../paths";
import { exists, listFiles, readText, writeText } from "../fs-utils";
import { SECTION_PREFIXES, loadRules } from "../rules";

const sectionNames: Record<string, string> = {
  arch: "Architecture & Dependency Injection",
  api: "API Contracts & Validation",
  security: "Security & Authorization",
  data: "Persistence & Transactions",
  errors: "Errors & Observability",
  cross: "Cross-Cutting Request Flow",
  perf: "Performance & Runtime",
  transport: "Transports & Integration Patterns",
  test: "Testing Strategy",
  ops: "Operations & Configuration",
};

export async function buildAgentsDocument(): Promise<void> {
  const rules = await loadRules();
  const datasetFiles = await listFiles(DATASET_DIR);
  const sourceFiles = await listFiles(SOURCES_DIR, ".json");
  const subskillsDir = join(SKILL_DIR, "subskills");
  const subskillFiles = (await exists(subskillsDir)) ? await listFiles(subskillsDir, ".md") : [];
  const skill = await readText(join(SKILL_DIR, "SKILL.md"));
  const metadata = JSON.parse(await readText(join(SKILL_DIR, "metadata.json"))) as {
    version?: string;
    frameworkVersion?: string;
  };
  const description = skill.match(/^description:\s*(.*)$/m)?.[1] ?? "NestJS best practices";

  const lines = [
    "# NestJS Best Practices",
    "",
    "> Generated by `bun run build`. Edit `SKILL.md`, `references/`, `dataset/`, or `sources/` instead.",
    "",
    "## Skill Description",
    "",
    description,
    "",
    `Version: ${metadata.version ?? "unknown"}; NestJS baseline: ${metadata.frameworkVersion ?? "unknown"}.`,
    "",
    "## Navigation",
    "",
    "Read `SKILL.md` first. Load only the rule files and dataset cases relevant to the task.",
    "",
    "## External Skill Packs",
    "",
    "For deep Prisma guidance, suggest using `$find-skills` to install the Prisma skill pack. This shared NestJS skill does not bundle Prisma source skills or Prisma subskills.",
    "",
    "## Rule Sections",
    "",
  ];

  for (const prefix of SECTION_PREFIXES) {
    const sectionRules = rules.filter((rule) => rule.prefix === prefix);
    lines.push(`### ${sectionNames[prefix]} (${prefix}-)`, "");
    for (const rule of sectionRules) {
      lines.push(`- \`${rule.id}\` (${rule.impact}) - ${rule.title}`);
    }
    lines.push("");
  }

  lines.push("## Dataset Cases", "");
  for (const file of datasetFiles) {
    lines.push(`- \`${file.relativePath}\``);
  }
  if (subskillFiles.length > 0) {
    lines.push("", "## Subskills", "");
    for (const file of subskillFiles) {
      lines.push(`- \`subskills/${file.relativePath}\``);
    }
  }
  lines.push("", "## Sources", "");
  for (const file of sourceFiles) {
    lines.push(`- \`sources/${file.relativePath}\``);
  }
  lines.push("");

  await writeText(join(SKILL_DIR, "AGENTS.md"), lines.join("\n"));
}
